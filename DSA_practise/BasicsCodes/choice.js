function findChoice(n){
    switch (n){
        case 1: console.log("choice is 1")
        break;
        case 2: console.log("choice is 2")
        break;
        case 3: console.log("choice is 3")
        break;
        case 4:console.log("choice is 4")
        break;
        default: console.log("choice other than 1,2,3 & 4")
        break;
    }
}
let n=2
findChoice(n)